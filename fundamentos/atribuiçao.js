// atrobuiçao nada mais é que alocar um valor sobre outro
// o sinal de = ja é uma atribuiçao 
const a = 7
let b = 3
// atribuiçoes mais especificas 

b += a // b = b + a 
console.log(b)

b -= a // b = b - a
console.log(b)

b *= a // b = b * a
console.log(b)

b /= a // b = b / a
console.log(b)

b %= 2 // b = b % a
console.log(b)